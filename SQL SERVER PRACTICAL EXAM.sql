(102,'ROY','TEACHING',50000,'2020-04-14'),
(103,'SASHA','FINANCE',95000,'2021-07-04'),
(104,'ARJUN','IT',70000,'2023-11-25'),
(105,'HEMA','TEACHING',52000,'2021-05-14'),
(106,'INDU','CODING',195000,'2019-04-04'),
(104,'ARJUN','IT',70000,'2023-11-25'),
(102,'ROY','TEACHING',50000,'2020-04-14');

SELECT * FROM EMPLOYEEDUPLICATE;

SELECT EMP_ID, 
       EMP_NAME, 
       DEPARTMENT, 
       SALARY,
       JOINDATE,
       COUNT(*) AS DUPECOUNT
FROM EMPLOYEEDUPLICATE
GROUP BY EMP_ID,
         EMP_NAME, 
         DEPARTMENT, 
         SALARY,
         JOINDATE
HAVING COUNT(*)>1;

SELECT DISTINCT EMP_ID, 
                EMP_NAME, 
                DEPARTMENT, 
                SALARY,
                JOINDATE
FROM EMPLOYEEDUPLICATE;

WITH DUPEEMP AS 
(
SELECT *,
ROW_NUMBER() OVER 
(
PARTITION BY EMP_ID, 
             EMP_NAME, 
             DEPARTMENT, 
             SALARY, 
             JOINDATE
             ORDER BY EMP_ID
) 
AS ROWNUM
FROM EMPLOYEEDUPLICATE
)
DELETE FROM DUPEEMP
WHERE ROWNUM>1;

SELECT * FROM EMPLOYEEDUPLICATE;

