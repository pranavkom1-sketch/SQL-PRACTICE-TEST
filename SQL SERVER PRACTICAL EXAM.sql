--QUESTION3

SELECT EMP_ID,
       EMP_NAME,
       SALARY,
       AVG(SALARY) OVER() AS AVGSAL
FROM EMPLOYEE;

SELECT EMP_ID,
       EMP_NAME,
       SALARY,
       ROW_NUMBER() OVER(ORDER BY EMP_ID) 
       AS ROWNUM
FROM EMPLOYEE;

SELECT EMP_ID,
       EMP_NAME,
       SALARY,
       RANK() OVER(ORDER BY SALARY DESC) 
       AS SALRANK
FROM EMPLOYEE;

