CREATE TABLE ENROLLMENTS
(
Enroll_ID INT PRIMARY KEY,
STUDENT_ID INT,
COURSE_ID INT,

FOREIGN KEY (STUDENT_ID) REFERENCES STUDENTS(STUDENT_ID), --ENSURES STUDENT_ID MUST EXIST IN STUDENTS TABLE
FOREIGN KEY (COURSE_ID) REFERENCES COURSES(COURSE_ID)     --ENSURES COURSE_ID MUST EXIST IN COURSES TABLE
);

