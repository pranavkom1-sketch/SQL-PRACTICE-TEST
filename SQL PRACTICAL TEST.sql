CREATE DATABASE STUDENTDBS;

CREATE TABLE STUDENTS
(
STUDENT_ID INT PRIMARY KEY,         --ENSURES EACH STUDENT HAS A UNIQUE IDENTIFIER
STUDENT_NAME VARCHAR(30) NOT NULL,  --ENSURES STUDENT NAME CANNOT BE EMPTY
AGE INT CHECK(AGE>=18),             --ENSURES ONLY STUDENTS AGED ABOVE 18 ARE ENTERED, OTHERWISE ERROR
CITY VARCHAR(30) DEFAULT 'KOCHI'    --IF NO CITY IS PROVIDED, DEFAULTS TO KOCHI
);

